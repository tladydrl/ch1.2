# ~ ( 역, 인버트)
print(~5)  # -6이 나온다..
print(~-1)

# << 연산자
a = 4
print(a >> 1)

# and, or, exclusive or
a = 3
print(a & 2)
print(a | 8)
print(0x0f ^ 0x06)
print(-2 << 1)

